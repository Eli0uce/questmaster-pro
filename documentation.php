                <!-- Installation -->
                <h2>Installation</h2>
                <p>Pour installer le plugin QuestMaster Pro, suivez ces étapes :</p>
                <ol>
                    <li>Téléchargez le fichier ZIP du plugin.</li>
                    <li>Connectez-vous à votre tableau de bord WordPress.</li>
                    <li>Allez dans "Extensions" -> "Ajouter une nouvelle".</li>
                    <li>Cliquez sur "Téléverser un plugin" et téléchargez le fichier ZIP.</li>
                    <li>Activez le plugin une fois l'installation terminée.</li>
                </ol>

                <!-- Configuration -->
                <h2>Configuration</h2>
                <p>Après l'installation, vous devrez configurer le plugin comme suit :</p>
                <ol>
                    <li>Allez dans "Réglages" -> "QuestMaster Pro" dans le menu de votre tableau de bord.</li>
                    <li>Configurez les options selon vos besoins, telles que l'activation du jeu et la page pour
                        afficher le jeu.</li>
                    <li>Cliquez sur "Enregistrer les paramètres" pour sauvegarder vos configurations.</li>
                </ol>

                <!-- Utilisation -->
                <h2>Utilisation</h2>
                <p>Une fois le plugin configuré, voici comment l'utiliser :</p>
                <ol>
                    <li>Créez une nouvelle page ou utilisez une page existante pour afficher le jeu de rôle.</li>
                </ol>

                <!-- Gestion des Armes -->
                <h2>Gestion des Armes</h2>
                <p>Notre plugin QuestMaster Pro vous permet également de gérer des armes. Voici comment faire :</p>
                <ol>
                    <li>Allez dans "Gestion des Armes" dans le menu de votre tableau de bord.</li>
                    <li>Vous pouvez ajouter de nouvelles armes en remplissant le formulaire d'ajout.</li>
                    <li>Consultez la liste des armes existantes dans la table des armes.</li>
                </ol>

